#include <stdio.h>

/**
 * add - add 2 integers
 * @a: int
 * @b: int
 * Return: int
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract 2 integers
 * @a: int
 * @b: int
 * Return: int
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply 2 integers
 * @a: int
 * @b: int
 * Return: int
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide 2 integers
 * @a: int
 * @b: int
 * Return: int
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod of 2 integers
 * @a: int
 * @b: int
 * Return: int
 */

int mod(int a, int b)
{
	return (a % b);
}
